import streamlit as st
import numpy as np
import datetime

from utils import (
    load_nhanes_model_and_scaler,
    build_nhanes_feature_df,
    RACE_OPTIONS,
)

st.title("🩺 Diabetes Risk Prediction (NHANES Model)")

st.markdown("""
Provide patient details and the model will estimate the **probability of Type 2 Diabetes**  
based on the tuned XGBoost model trained on NHANES.

> ⚠️ **Disclaimer:** This tool is for research and educational purposes only.  
> It must **not** be used as a substitute for professional medical advice or diagnosis.
""")


@st.cache_resource
def get_model_and_scaler():
    return load_nhanes_model_and_scaler()


model, scaler = get_model_and_scaler()


def build_report_text(inputs: dict, risk_pct: float, risk_level: str):
    lines = []
    lines.append("Explainable & Fair AI for Type 2 Diabetes Risk")
    lines.append("Generated report")
    lines.append(f"Date: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}")
    lines.append("")
    lines.append("=== Patient Inputs ===")
    for k, v in inputs.items():
        lines.append(f"{k}: {v}")
    lines.append("")
    lines.append("=== Model Output ===")
    lines.append(f"Estimated diabetes risk: {risk_pct:.1f}%")
    lines.append(f"Risk category: {risk_level}")
    lines.append("")
    lines.append("Note: This prediction is generated by a machine learning model trained on NHANES data. "
                 "It is not a medical diagnosis.")
    return "\n".join(lines)


with st.form("risk_form"):
    col1, col2, col3 = st.columns(3)

    with col1:
        gender = st.selectbox("Gender", ["Male", "Female"])
        age = st.number_input("Age (years)", min_value=18, max_value=120, value=45)
        bmi = st.number_input("Body Mass Index (BMI)", min_value=10.0, max_value=70.0, value=28.0, step=0.1)

    with col2:
        hba1c = st.number_input("HbA1c (%)", min_value=3.0, max_value=15.0, value=5.8, step=0.1)
        glucose = st.number_input("Fasting Glucose (mg/dL)", min_value=50.0, max_value=400.0, value=95.0, step=1.0)

    with col3:
        income_ratio = st.number_input(
            "Income-to-Poverty Ratio (INDFMPIR)",
            min_value=0.0, max_value=10.0, value=2.0, step=0.1
        )

        race_label = st.selectbox("Race / Ethnicity (simplified)", list(RACE_OPTIONS.keys()))

    submitted = st.form_submit_button("Predict Risk")


if submitted:
    # Build feature df
    X_single = build_nhanes_feature_df(
        gender=gender,
        age=age,
        bmi=bmi,
        hba1c=hba1c,
        glucose=glucose,
        income_ratio=income_ratio,
        race_label=race_label,
    )

    # Scale
    X_scaled = scaler.transform(X_single.values)

    # Predict
    proba = model.predict_proba(X_scaled)[:, 1][0]
    pred = int(proba >= 0.5)
    risk_pct = proba * 100

    st.markdown("---")
    st.subheader("Prediction Result")

    col1, col2 = st.columns([2, 1])

    # Risk display + progress bar
    with col1:
        st.metric("Estimated Diabetes Risk", f"{risk_pct:.1f}%")

        # progress bar
        st.write("Risk level indicator")
        pct_for_bar = float(min(max(risk_pct / 100.0, 0.0), 1.0))
        st.progress(pct_for_bar)

        if risk_pct < 20:
            risk_level = "Low"
            color = "🟢"
            advice = "Maintain a healthy lifestyle (balanced diet, regular exercise, and routine check-ups)."
        elif risk_pct < 50:
            risk_level = "Moderate"
            color = "🟡"
            advice = "Consider lifestyle optimisation and regular screening. Discuss risk factors with a clinician."
        else:
            risk_level = "High"
            color = "🔴"
            advice = "This suggests a higher risk. A professional medical evaluation and targeted interventions are recommended."

        st.write(f"**Risk Category:** {color} **{risk_level}**")
        st.write("**Interpretation:**", advice)

    with col2:
        st.write("**Model Decision (threshold = 0.5):**")
        if pred == 1:
            st.error("Model predicts: **Diabetes / High Risk** (class = 1)")
        else:
            st.success("Model predicts: **No Diabetes / Lower Risk** (class = 0)")

        st.caption(
            "Threshold can be adjusted in future work (e.g., more sensitive vs more specific model)."
        )

    st.markdown("### Input Summary")
    input_summary = {
        "Gender": gender,
        "Age (years)": age,
        "BMI": bmi,
        "HbA1c (%)": hba1c,
        "Fasting Glucose (mg/dL)": glucose,
        "Income-to-Poverty Ratio (INDFMPIR)": income_ratio,
        "Race / Ethnicity": race_label,
    }
    st.write(input_summary)

    # Simple text report download
    report_text = build_report_text(input_summary, risk_pct, risk_level)
    report_bytes = report_text.encode("utf-8")

    st.download_button(
        label="📄 Download Risk Report (.txt)",
        data=report_bytes,
        file_name="diabetes_risk_report.txt",
        mime="text/plain",
    )

    st.caption("You may save this report for your medical records or share it with a healthcare professional.")
